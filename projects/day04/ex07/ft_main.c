#include <stdio.h>

int	ft_find_next_prime(int nb);

int	main(void)
{
	int next;
	int nb[] = {-2377, -1, 0, 1, 2, 70, 71, 2370, 2377,
		4580, 4583, 7910, 7919, 104720, 104729,
		2038074730, 2038074743, 2147483640, 2147483647};
	int c;

	c = 0;
	while (c < 19)
	{
		next = ft_find_next_prime(nb[c]);
		printf("The next prime after %d: %d\n", nb[c], next);
		++c;
	}
	return (0);
}
