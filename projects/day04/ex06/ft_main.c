#include <stdio.h>

int	ft_is_prime(int nb);

int	main(void)
{
	int isprime;
	int nb[] = {-2377, -1, 0, 1, 2, 71, 2377, 4583,
		7919, 104729, 2038074743, 2147483647,
		72, 2378, 4584, 7920, 104730};
	int c;

	c = 0;
	while (c < 17)
	{
		isprime = ft_is_prime(nb[c]);
		printf("Is %d a prime? %d\n", nb[c], isprime);
		++c;
	}
	return (0);
}
