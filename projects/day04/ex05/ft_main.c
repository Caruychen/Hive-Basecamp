#include <stdio.h>

int	ft_sqrt(int nb);

int	main(void)
{
	int sqrt;
	int nb[] = { -1307, 0, 214748364, 19793601, 51829802, 2033476, 177849891, 1590175129, 20969477, 680062084, 193084230, 1541426121, 155506239};
	int c;

	c = 0;
	while (c < 13)
	{
		sqrt = ft_sqrt(nb[c]);
		printf("Sqr root of %d = %d\n", nb[c], sqrt);
		++c;
	}
	return (0);
}
