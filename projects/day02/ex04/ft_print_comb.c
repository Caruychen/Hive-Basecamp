void	ft_putchar(char c);

void	ft_print_comb(void)
{
	char c[3];

	c[0] = '0';
	while (c[0] < '9' - 1)
	{
		c[1] = c[0] + 1;
		while (c[1] < '9')
		{
			c[2] = c[1] + 1;
			while (c[2] <= '9')
			{
				ft_putchar(c[0]);
				ft_putchar(c[1]);
				ft_putchar(c[2]);
				if (c[0] < '9' - 2)
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				c[2]++;
			}
			c[1]++;
		}
		c[0]++;
	}
}
