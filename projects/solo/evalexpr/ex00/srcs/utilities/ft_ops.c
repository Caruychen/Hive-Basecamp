int	ft_sum(int n1, int n2)
{
	return (n1 + n2);
}

int	ft_subtract(int n1, int n2)
{
	return (n1 - n2);
}

int	ft_multiply(int n1, int n2)
{
	return (n1 * n2);
}

int	ft_divide(int n1, int n2)
{
	return (n1 / n2);
}

int	ft_modulus(int n1, int n2)
{
	return (n1 % n2);
}
